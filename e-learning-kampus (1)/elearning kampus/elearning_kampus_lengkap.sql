-- =====================================================
-- DATABASE E-LEARNING KAMPUS (ECO-LEARNING PORTAL)
-- GABUNGAN LENGKAP: SCHEMA + INITIAL SEED DATA
-- Target: MySQL / MariaDB (XAMPP Localhost)
-- Generated programmatically for 100 Mahasiswa, 6 Dosen, 2 Admin
-- Paswword format: First Name + 3 Unique Random Digits (1-9)
