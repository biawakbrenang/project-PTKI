CREATE DATABASE IF NOT EXISTS elearning_kampus;
USE elearning_kampus;

-- -----------------------------------------------------
-- PART 1: CORE USERS & ROLES SCHEMA
-- -----------------------------------------------------

-- 1. Table: users (All active accounts in the portal)
CREATE TABLE IF NOT EXISTS users (
  id INT AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(50) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  nama_lengkap VARCHAR(150) NOT NULL,
  email VARCHAR(100) NOT NULL UNIQUE,
  role ENUM('admin', 'dosen', 'mahasiswa') NOT NULL,
  status ENUM('aktif', 'nonaktif') DEFAULT 'aktif',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- 2. Table: dosen (Lecturers/Instructors details)
CREATE TABLE IF NOT EXISTS dosen (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  nip VARCHAR(50) NOT NULL UNIQUE,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- 3. Table: mahasiswa (Students details)
CREATE TABLE IF NOT EXISTS mahasiswa (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  jurusan_id INT NOT NULL, -- 21 = Sistem Informasi, 22 = Teknik Informatika
  nim VARCHAR(50) NOT NULL UNIQUE,
  semester_saat_ini INT NOT NULL DEFAULT 1,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- -----------------------------------------------------
-- PART 2: ACADEMIC & ACTIVITIES SCHEMA
-- -----------------------------------------------------

-- 4. Table: matakuliah (Courses catalogue)
CREATE TABLE IF NOT EXISTS matakuliah (
  id INT AUTO_INCREMENT PRIMARY KEY,
  kode_matkul VARCHAR(20) NOT NULL UNIQUE,
  nama_matkul VARCHAR(150) NOT NULL,
  sks INT NOT NULL
);

-- 5. Table: kelas (Parallel classes assigned to lecturer and course)
CREATE TABLE IF NOT EXISTS kelas (
  id INT AUTO_INCREMENT PRIMARY KEY,
  matakuliah_id INT NOT NULL,
  dosen_id INT,
  kode_kelas VARCHAR(50) NOT NULL UNIQUE,
  semester INT NOT NULL,
  tahun_akademik VARCHAR(20) DEFAULT '2025/2026',
  hari VARCHAR(20) NOT NULL,
  jam_mulai TIME NOT NULL,
  jam_selesai TIME NOT NULL,
  ruangan VARCHAR(100) NOT NULL,
  kapasitas INT NOT NULL DEFAULT 40,
  jumlah_mahasiswa INT DEFAULT 0,
  FOREIGN KEY (matakuliah_id) REFERENCES matakuliah(id) ON DELETE CASCADE,
  FOREIGN KEY (dosen_id) REFERENCES dosen(id) ON DELETE SET NULL
);

-- 6. Table: krs (Course enrollment tracking with final grades)
CREATE TABLE IF NOT EXISTS krs (
  id INT AUTO_INCREMENT PRIMARY KEY,
  mahasiswa_id INT NOT NULL,
  kelas_id INT NOT NULL,
  matakuliah_id INT NOT NULL,
  semester INT NOT NULL,
  grade VARCHAR(2) DEFAULT NULL,
  FOREIGN KEY (mahasiswa_id) REFERENCES mahasiswa(id) ON DELETE CASCADE,
  FOREIGN KEY (kelas_id) REFERENCES kelas(id) ON DELETE CASCADE,
  FOREIGN KEY (matakuliah_id) REFERENCES matakuliah(id) ON DELETE CASCADE
);

-- 7. Table: pertemuan (Lectures sessions / meetings)
CREATE TABLE IF NOT EXISTS pertemuan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  kelas_id INT NOT NULL,
  nomor_pertemuan INT NOT NULL,
  judul_pertemuan VARCHAR(255),
  deskripsi TEXT,
  tanggal_pertemuan DATE,
  jam_mulai TIME,
  jam_selesai TIME,
  status ENUM('belum_dimulai', 'berlangsung', 'selesai') DEFAULT 'belum_dimulai',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (kelas_id) REFERENCES kelas(id) ON DELETE CASCADE,
  UNIQUE KEY unique_pertemuan (kelas_id, nomor_pertemuan),
  INDEX idx_tanggal (tanggal_pertemuan)
);

-- 8. Table: materi_pertemuan (Slide and documents uploads)
CREATE TABLE IF NOT EXISTS materi_pertemuan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  pertemuan_id INT NOT NULL,
  judul_materi VARCHAR(255) NOT NULL,
  deskripsi TEXT,
  file_path VARCHAR(255) NOT NULL,
  file_name VARCHAR(255) NOT NULL,
  file_size INT NOT NULL,
  tipe_file VARCHAR(50) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (pertemuan_id) REFERENCES pertemuan(id) ON DELETE CASCADE
);

-- 9. Table: tugas_pertemuan (Lecturer published assignments)
CREATE TABLE IF NOT EXISTS tugas_pertemuan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  pertemuan_id INT NOT NULL,
  judul_tugas VARCHAR(255) NOT NULL,
  deskripsi TEXT,
  tanggal_deadline DATETIME NOT NULL,
  bobot_nilai INT DEFAULT 10,
  status ENUM('aktif', 'selesai') DEFAULT 'aktif',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (pertemuan_id) REFERENCES pertemuan(id) ON DELETE CASCADE
);

-- 10. Table: submission_tugas_pertemuan (Students submissions & reviews)
CREATE TABLE IF NOT EXISTS submission_tugas_pertemuan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  tugas_pertemuan_id INT NOT NULL,
  mahasiswa_id INT NOT NULL,
  file_path VARCHAR(255) NOT NULL,
  file_name VARCHAR(255) NOT NULL,
  file_size INT NOT NULL,
  catatan_mahasiswa TEXT,
  nilai INT DEFAULT NULL,
  komentar_dosen TEXT,
  status ENUM('submitted', 'graded') DEFAULT 'submitted',
  tanggal_submit DATETIME NOT NULL,
  tanggal_graded DATETIME DEFAULT NULL,
  FOREIGN KEY (tugas_pertemuan_id) REFERENCES tugas_pertemuan(id) ON DELETE CASCADE,
  FOREIGN KEY (mahasiswa_id) REFERENCES mahasiswa(id) ON DELETE CASCADE
);

-- 11. Table: absensi (Students attendance registry)
CREATE TABLE IF NOT EXISTS absensi (
  id INT AUTO_INCREMENT PRIMARY KEY,
  krs_id INT NOT NULL,
  pertemuan INT NOT NULL,
  tanggal DATE NOT NULL,
  status ENUM('hadir', 'sakit', 'izin', 'alfa') NOT NULL,
  keterangan VARCHAR(255),
  FOREIGN KEY (krs_id) REFERENCES krs(id) ON DELETE CASCADE
);

